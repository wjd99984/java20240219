package ch04.lecture.p1if.p2switch;

public class C01Switch {
    public static void main(String[] args) {

        System.out.println("statmant 1");

        //값이 "서울"
        switch ("제주") {
            case "서울":
                System.out.println("statmant 2");
                break;
            //값이 "제주"
            case "제주":
                System.out.println("statmant 3");
                break;
            //값이"부산"
            case "부산":
                System.out.println("statmant 4");
                break;
        }
        System.out.println("statmant 5");

        System.out.println("statmant 6");

        System.out.println("statmant 7");

        System.out.println("statmant 8");

        System.out.println("statmant 9");
    }
}
